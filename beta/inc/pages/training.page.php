<h3> <i>Dinghy Training</i></h3>
<p>Beginners courses are normally held on Sundays and Thursday evenings between 1st May and 30 September.  For both adults and youngsters, the course equates to approximately 4 days training.<br/><br/>

We have a fleet of training dinghies to suit youngsters and adults:
<ul>
<li>Optimists for children from the age of eight</li>
<li>Topaz UNOs (new in 2010) suitable for youngsters and adults</li>
<li>Toppers for the young sailor</li>
<li>Lasers - for the more competent adult</li>
<li>RS Vision and a Topper Omega for those who wish to learn and develop their skills in a multi person or family dinghy.
</ul>

<p>For more information, or to book a place, please contact our chief instructor Jeremy Martin on 01656 861092 (e-mail <a href="mailto:training@tatasteelsailing.org.uk">training@tatasteelsailing.org.uk</a>).  If not available at the time, you can contact any of our RYA qualified dinghy instructors - see contacts page on this website<br/><hr/>

