	<h1 class="page-header">1. Rules</h1>
	<p>Racing will be governed by the ‘Rules’ defined in <i>The Racing Rules of Sailing</i> and these Instructions which change <i>RRS</i> 32, A2, A3 and Race Signal AP.</p>
	
	<h1 class="page-header">2. Notice of Competitors</h1>
	<p>Notices to competitors and any changes to these Instructions will be posted on the club website.</p>

	<h1 class="page-header">3. Briefing</h1>
	<p>Anyone wanting to have a race brief should contact the Race Officer.</p>

	<h1 class="page-header">4. Signals made ashore</h1>
	<p>Signals made ashore will be hoisted at the signal mast at the Race Control hut.</p>
	<p>If ‘AP’ is displayed ashore, it will be removed (with sound signal) at least 10 minutes before the next warning signal. This changes Race Signal AP.</p>

	<h1 class="page-header">5 Schedule of races</h1>
	<p>The numbers of races in a series are approximations; they may be slightly less owing to open meetings which are yet
		to be finalised.If less than the qualifying numbers of races are sailed, then the number of races to qualify
		will be the total number of races sailed in that series.
	</p>
