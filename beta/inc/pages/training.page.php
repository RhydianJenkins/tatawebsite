<h3><i>Tata Steel Sailing Club Margam - Training</i></h3>

<p>Training and membership of the club is open to almost anyone (link to <a href="Membership.html">membership page</a> )
<p>
You do not need to be employed by Tata Steel, however, you will need to join the club first before starting a training course. </p>
<p>
The club is recognised by the Royal Yachting Association (RYA) as an approved training centre offering dinghy sailing, powerboat, safety boat, and 1st aid courses.  Our instructors hold the appropriate RYA qualifications.</p>
<p>
The sail training programmes are suitable for ages 8 to 80+</p>
<p>
You don't have to be able to swim, but it is an aid to confidence on the water</p>
<p>
Why not visit the club on a Sunday to gain an understanding of our activities.  Any of our members will be able to direct you to one of our instructors.</p><p>
