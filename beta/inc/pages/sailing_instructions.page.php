	<h1 class="page-header">6. Class Flags</h1>
	<p>6.1 The Class Flags used:</p>
		<li>Flag G will be used for the General Handicap</li>
		<li>Numeral ‘1’ will be used for the Solo class</li>
		<li>Laser Class flag will be used for the Laser Class</li>
	
	<p>6.2 Other flags can be used for other classes as appropriate. This will be highlighted by
		the OD at least 10 minutes before the start of the race</p>
		
	<h1 class="page-header">7. Racing area, course plan and marks</h1>
	<p>7.1 These will be shown on the board at the Race Control hut.</p>
	
	<p>7.2 Boats will sail the course as shown on the course plan, and will be required to sail through the start/finish line at the end of every lap</p>
	
	<h1 class="page-header">8. The Start</h1>
	<p>The start line for a shore start will be shown by a transit from two masts on the shore, and an outer distance mark which may not be 
	exactly in line with the transit. An inner distance mark may be laid, and in this case boats approaching the line to start shall pass 
	between this mark and the outer distance mark</p>
	
	<h1 class="page-header">9. Change of leg of the course</h1>
	<p>This is not allowed in G/H racing</p>
	
	<h1 class="page-header">10. The Finish</h1>
	<p>10.1 Each boat will sail the defined number of laps or until flag 'S' is displayed, with class flag(s) if only selected class(es) are
	to finish. The leading boat of each class will then finish when next crossing the finishing line and other boats will finish thereafter.
	Any boat more than one lap behind a class leader may be finished on next crossing the finishing line, and given the position held at that time.
		This changes <i>RRS</i> 32 and A3</p>
	<p>10.2 The <b>finishing line</b> will be between the mast at the OD hut and the finishing mark, or between a staff on a support boat
		displaying flag ‘S’ and the adjacent rounding mark.</p>
		
	<h1 class="page-header">11. Scoring</h1>
	<p>11.1 The events will be scored using <i>RRS</i> Appendix A apart from those detailed in the table in paragraph 5</p>
	<p>11.2 These are changes to <i>RRS</i> A2.</p>
	
	<h1 class="page-header">12. Penalties</h1>
	<p>Penalties will apply in accordance with <i>RRS</i> 44.</p>
	
	<h1 class="page-header">13. Time Limit</h1>
	<p>13.1 It is intended to race for approximately 60 minutes. Failure to achieve this shall not be cause for redress. </p>
	<p>13.2 Any boat not finishing within 20 minutes of the leading boat may be finished on the course by a support boat displaying flag W.</p>
	<p>13.3 In the event of abandonment, places may be awarded from positions at the end of the previous completed lap</p>
	
	<h1 class="page-header">14. Protests</h1>
	<p>Protests shall be written on forms available at the Committee Room and needs to be delivered to the Race Officer within Protest Time 
	which will begin as the last boat comes ashore at the end of each sailing session and end 30 minutes later</p>

